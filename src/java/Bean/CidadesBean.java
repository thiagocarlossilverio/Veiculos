package Bean;

/**
 *
 * @author TCS
 */
public class CidadesBean {
 
    private int idcidades;
    private int idestados;
    private String uf;
    private String cidade;

    /**
     * @return the idcidades
     */
    public int getIdcidades() {
        return idcidades;
    }

    /**
     * @param idcidades the idcidades to set
     */
    public void setIdcidades(int idcidades) {
        this.idcidades = idcidades;
    }

    /**
     * @return the idestados
     */
    public int getIdestados() {
        return idestados;
    }

    /**
     * @param idestados the idestados to set
     */
    public void setIdestados(int idestados) {
        this.idestados = idestados;
    }

    /**
     * @return the uf
     */
    public String getUf() {
        return uf;
    }

    /**
     * @param uf the uf to set
     */
    public void setUf(String uf) {
        this.uf = uf;
    }

    /**
     * @return the cidade
     */
    public String getCidade() {
        return cidade;
    }

    /**
     * @param cidade the cidade to set
     */
    public void setCidade(String cidade) {
        this.cidade = cidade;
    }
    
}
